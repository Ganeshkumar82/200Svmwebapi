const express = require("express");
const cron = require("node-cron");
const router = express.Router();
const db = require("../services/db");
const redis = require("../services/redis");

//######################################################################################################################################################################################################
//######################################################################################################################################################################################################
//######################################################################################################################################################################################################

router.post("/updatedevice", async function (req, res, next) {
  try {
    res.json(await redis.UpdateDevice(req.body));
  } catch (er) {
    console.error(`Error while Updating the Device to the redis ${er.message}`);
    next(er);
  }
});

//######################################################################################################################################################################################################
//######################################################################################################################################################################################################

router.post("/geteventid", async function (req, res, next) {
  try {
    res.json(await redis.getEventId(req.body));
  } catch (er) {
    console.error(`Error while gettting the device id ${er.message}`);
    next(er);
  }
});

